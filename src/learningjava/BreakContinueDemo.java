public class BreakContinueDemo {

	public static void main(String[] args) {
	for (int i=1; i<=12;i++)
	{
		if (i==5) {
			continue;   //continue is use to skip some data from the iteration
			}
		if (i==8) {
			break;
			}
			System.out.println(i);
			
			}
	System.out.println("break ");
	
	
	
    }

	}


